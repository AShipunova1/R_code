# replace sourcing files with "file = "
comment_out_sources <-
  function(my_text) {
    my_res_text <-
      stringr::str_replace(my_text,
        "^\\s*[^#]*source\\((.+)\\)", 
"#+, file = \\1"
      )
    return(my_res_text)
  }

